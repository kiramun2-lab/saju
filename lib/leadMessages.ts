/** 입력 조건(이름, 성별, 생년월일, 시간 등)을 같은 값이면 같은 숫자가 나오는 시드로 변환 */
export function hashSajuInput(input: {
  name: string;
  relation: string;
  birthDate: string;
  calendarType: string;
  birthTime: string;
  gender: string;
}): number {
  const str =
    [input.birthDate, input.birthTime, input.gender, input.name, input.relation, input.calendarType].join('|');
  let h = 5381;
  for (let i = 0; i < str.length; i++)
    h = ((h << 5) + h) + str.charCodeAt(i);
  return h >>> 0;
}

/** 섹션 id에 해당하는 리드 메시지 목록에서 하나를 반환 (indexOrSeed 있으면 결정론적) */
export function getLeadForSection(
  sectionId: string,
  indexOrSeed?: number
): string {
  const list = LEAD_MESSAGES[sectionId];
  if (!list?.length) return '';
  const idx =
    indexOrSeed !== undefined
      ? Math.abs(Math.floor(indexOrSeed)) % list.length
      : Math.floor(Math.random() * list.length);
  return list[idx];
}

/** 10개 섹션에 대해 각각 리드 1개씩. seed 있으면 같은 조건 → 같은 리드 조합 보장 */
export function getLeadsForResult(seed?: number): string[] {
  const areas = [
    'overall',
    'love',
    'career',
    'attract',
    'strength',
    'path',
    'talent',
    'money',
    'relation',
    'future',
  ];
  return areas.map((id, i) =>
    getLeadForSection(id, seed !== undefined ? seed + i : undefined)
  );
}
